namespace SmoothScroll.Avalonia.Controls;

/// <summary>
/// Defines how a <see cref="ScrollView"/> displays a scroll bar.
/// </summary>
public enum ScrollBarVisibilityMode
{
    /// <summary>
    /// Shows the scroll bar when its axis is enabled for scrolling.
    /// </summary>
    Auto,

    /// <summary>
    /// Always shows the scroll bar.
    /// </summary>
    Visible,

    /// <summary>
    /// Always hides the scroll bar.
    /// </summary>
    Hidden
}
